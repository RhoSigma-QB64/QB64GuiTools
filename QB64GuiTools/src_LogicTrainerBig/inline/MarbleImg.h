// --- Saved full qualified output path and filename, so we've no troubles
// --- when cleaning up, even if the current working folder was changed
// --- during program runtime.
// ---------------------------------------------------------------------
char MarbleImgName[8192]; // it's a safe size for any current OS

// --- Cleanup function to delete the written file, called by the atexit()
// --- handler at program termination time, if requested by user.
// ---------------------------------------------------------------------
void KillMarbleImgData(void)
{
    remove(MarbleImgName);
}

// --- Function to write the array(s) back into a file, will return the
// --- full qualified output path and filename on success, otherwise an
// --- empty string is returned (access/write errors, file truncated).
// ---------------------------------------------------------------------
const char *WriteMarbleImgData(const char *FileName, int16 AutoClean)
{
    FILE *han = NULL; // file handle
    int32 num = NULL; // written elements

    #ifdef QB64_WINDOWS
    if (!_fullpath(MarbleImgName, FileName, 8192)) return "";
    #else
    if (!realpath(FileName, MarbleImgName)) return "";
    #endif

    if (!(han = fopen(MarbleImgName, "wb"))) return "";
    if (AutoClean) atexit(KillMarbleImgData);

    num = fwrite(&MarbleImgL0[1], 4, MarbleImgL0[0], han);
    if (num != MarbleImgL0[0]) {fclose(han); return "";}

    num = fwrite(&MarbleImgB[1], 1, MarbleImgB[0], han);
    if (num != MarbleImgB[0]) {fclose(han); return "";}

    fclose(han);
    return MarbleImgName;
}

