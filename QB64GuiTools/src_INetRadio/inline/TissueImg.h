// --- Saved full qualified output path and filename, so we've no troubles
// --- when cleaning up, even if the current working folder was changed
// --- during program runtime.
// ---------------------------------------------------------------------
char TissueImgName[8192]; // it's a safe size for any current OS

// --- Cleanup function to delete the written file, called by the atexit()
// --- handler at program termination time, if requested by user.
// ---------------------------------------------------------------------
void KillTissueImgData(void)
{
    remove(TissueImgName);
}

// --- Function to write the array(s) back into a file, will return the
// --- full qualified output path and filename on success, otherwise an
// --- empty string is returned (access/write errors, file truncated).
// ---------------------------------------------------------------------
const char *WriteTissueImgData(const char *FileName, int16_t AutoClean)
{
    FILE   *han = NULL; // file handle
    int32_t num = NULL; // written elements

    #ifdef QB64_WINDOWS
    if (!_fullpath(TissueImgName, FileName, 8192)) return "";
    #else
    if (!realpath(FileName, TissueImgName)) return "";
    #endif

    if (!(han = fopen(TissueImgName, "wb"))) return "";
    if (AutoClean) atexit(KillTissueImgData);

    num = fwrite(&TissueImgL0[1], 4, TissueImgL0[0], han);
    if (num != TissueImgL0[0]) {fclose(han); return "";}

    num = fwrite(&TissueImgB[1], 1, TissueImgB[0], han);
    if (num != TissueImgB[0]) {fclose(han); return "";}

    fclose(han);
    return TissueImgName;
}

