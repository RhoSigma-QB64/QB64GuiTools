// --- Saved full qualified output path and filename, so we've no troubles
// --- when cleaning up, even if the current working folder was changed
// --- during program runtime.
// ---------------------------------------------------------------------
char BackImgName[8192]; // it's a safe size for any current OS

// --- Cleanup function to delete the written file, called by the atexit()
// --- handler at program termination time, if requested by user.
// ---------------------------------------------------------------------
void KillBackImgData(void)
{
    remove(BackImgName);
}

// --- Function to write the array(s) back into a file, will return the
// --- full qualified output path and filename on success, otherwise an
// --- empty string is returned (access/write errors, file truncated).
// ---------------------------------------------------------------------
const char *WriteBackImgData(const char *FileName, int16_t AutoClean)
{
    FILE   *han = NULL; // file handle
    int32_t num = NULL; // written elements

    #ifdef QB64_WINDOWS
    if (!_fullpath(BackImgName, FileName, 8192)) return "";
    #else
    if (!realpath(FileName, BackImgName)) return "";
    #endif

    if (!(han = fopen(BackImgName, "wb"))) return "";
    if (AutoClean) atexit(KillBackImgData);

    num = fwrite(&BackImgL0[1], 4, BackImgL0[0], han);
    if (num != BackImgL0[0]) {fclose(han); return "";}

    num = fwrite(&BackImgB[1], 1, BackImgB[0], han);
    if (num != BackImgB[0]) {fclose(han); return "";}

    fclose(han);
    return BackImgName;
}

