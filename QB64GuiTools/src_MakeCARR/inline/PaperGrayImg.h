// --- Saved full qualified output path and filename, so we've no troubles
// --- when cleaning up, even if the current working folder was changed
// --- during program runtime.
// ---------------------------------------------------------------------
char PaperGrayImgName[8192]; // it's a safe size for any current OS

// --- Cleanup function to delete the written file, called by the atexit()
// --- handler at program termination time, if requested by user.
// ---------------------------------------------------------------------
void KillPaperGrayImgData(void)
{
    remove(PaperGrayImgName);
}

// --- Function to write the array(s) back into a file, will return the
// --- full qualified output path and filename on success, otherwise an
// --- empty string is returned (access/write errors, file truncated).
// ---------------------------------------------------------------------
const char *WritePaperGrayImgData(const char *FileName, int16_t AutoClean)
{
    FILE   *han = NULL; // file handle
    int32_t num = NULL; // written elements

    #ifdef QB64_WINDOWS
    if (!_fullpath(PaperGrayImgName, FileName, 8192)) return "";
    #else
    if (!realpath(FileName, PaperGrayImgName)) return "";
    #endif

    if (!(han = fopen(PaperGrayImgName, "wb"))) return "";
    if (AutoClean) atexit(KillPaperGrayImgData);

    num = fwrite(&PaperGrayImgL0[1], 4, PaperGrayImgL0[0], han);
    if (num != PaperGrayImgL0[0]) {fclose(han); return "";}

    num = fwrite(&PaperGrayImgB[1], 1, PaperGrayImgB[0], han);
    if (num != PaperGrayImgB[0]) {fclose(han); return "";}

    fclose(han);
    return PaperGrayImgName;
}

